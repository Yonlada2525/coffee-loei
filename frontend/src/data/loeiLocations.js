export const LOEI_DISTRICTS = Object.keys(LOEI_LOCATIONS);

export function getSubDistricts(district) {
  return district ? (LOEI_LOCATIONS[district] || []) : [];
}

export function getVillages(district, subDistrict) {
  const found = getSubDistricts(district).find((item) => item.subDistrict === subDistrict);
  return found?.villages || [];
}

export function getPostalCode(district, subDistrict) {
  const found = getSubDistricts(district).find((item) => item.subDistrict === subDistrict);
  return found?.postalCode || '';
}
